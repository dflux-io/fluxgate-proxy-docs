// Generates src/generated/pageFiles.ts: a slug -> source-file map plus the
// GitHub edit base, parsed from App.tsx routes. DocPage uses it to render an
// "Edit this page on GitHub" link without threading a file path through every
// page. Also generates public/llms.txt — a plain-text index of the docs for
// LLM/agent consumption (the emerging /llms.txt convention).
//
// Run: node scripts/build-pagemeta.mjs   (also runs from dev/build)

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const APP = resolve(root, 'src/App.tsx');
const MANIFEST = resolve(root, 'src/manifest.ts');
const PKG = JSON.parse(readFileSync(resolve(root, 'package.json'), 'utf8'));
const OUT_TS = resolve(root, 'src/generated/pageFiles.ts');
const OUT_LLMS = resolve(root, 'public/llms.txt');

const SITE_URL = (process.env.SITE_URL || `https://${PKG.name}.vercel.app`).replace(/\/+$/, '');
const EDIT_BASE = `https://github.com/dflux-io/${PKG.name}/blob/main/`;

// --- slug -> file, from App.tsx ---
const app = readFileSync(APP, 'utf8');
const comp = {}; // componentName -> src/pages/<path>.tsx
for (const m of app.matchAll(/(?:const\s+(\w+)\s*=\s*lazy\(\(\)\s*=>\s*import|import\s+(\w+)\s+from)\s*\(?['"]\.\/pages\/([^'"]+)['"]/g)) {
  const name = m[1] || m[2];
  comp[name] = `src/pages/${m[3]}.tsx`;
}
const pageFiles = {};
for (const m of app.matchAll(/<Route\s+path="([^"]+)"\s+element=\{<(\w+)\s*\/>\}/g)) {
  const [, path, name] = m;
  if (path === '*') continue;
  const slug = path === '/' ? '' : path.replace(/^\//, '');
  if (comp[name]) pageFiles[slug] = comp[name];
}

mkdirSync(dirname(OUT_TS), { recursive: true });
writeFileSync(
  OUT_TS,
  '// AUTO-GENERATED by scripts/build-pagemeta.mjs — do not edit by hand.\n' +
    `export const repoEditBase = '${EDIT_BASE}';\n` +
    `export const pageFiles: Record<string, string> = ${JSON.stringify(pageFiles, null, 2)};\n`,
);

// --- public/llms.txt, from the manifest ---
const man = readFileSync(MANIFEST, 'utf8');
const projectName = man.match(/const projectName = '([^']+)'/)?.[1] ?? PKG.name;
const groups = []; // {title, pages:[{slug,title}]}
const groupRe = /title:\s*'([^']+)',\s*\n\s*pages:\s*\[([\s\S]*?)\],\s*\n\s*\}/g;
for (const g of man.matchAll(groupRe)) {
  const pages = [...g[2].matchAll(/\{\s*slug:\s*'([^']*)'\s*,\s*title:\s*'([^']+)'/g)].map((p) => ({ slug: p[1], title: p[2] }));
  if (pages.length) groups.push({ title: g[1], pages });
}

let llms = `# ${projectName} docs\n\n> ${PKG.description || ''}\n\n`;
for (const g of groups) {
  llms += `## ${g.title}\n`;
  for (const p of g.pages) llms += `- [${p.title}](${SITE_URL}/${p.slug})\n`;
  llms += '\n';
}
mkdirSync(dirname(OUT_LLMS), { recursive: true });
writeFileSync(OUT_LLMS, llms);

console.log(`[build-pagemeta] wrote src/generated/pageFiles.ts (${Object.keys(pageFiles).length} pages) + public/llms.txt`);
